
let subject1 = 95; // This will give 'A'
let subject2 = 75; // This will give 'B'
let subject3 = 60; // This will give 'C'

let averageScore = (subject1 + subject2 + subject3) / 3;

if (averageScore > 90 && averageScore <= 100) {
    console.log("A");
} else if (averageScore > 70 && averageScore <= 80) {
    console.log("B");
} else if (averageScore > 50 && averageScore <= 65) {
    console.log("C");
} else if (averageScore > 25 && averageScore <= 50) {
    console.log("D");
} else {
    console.log("Go study and come back later");
}




















