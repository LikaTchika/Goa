
//  Examples of Incrementis (++)
let a = 1;
a++;  // a = 2
let b = 5;
b++;  // b = 6
let c = 10;
c++;  // c = 11
let d = 100;
d++;  // d = 101
let e = -3;
e++;  // e = -2

//   Examples of Decrementis (--)
let f = 5;
f--;  // f = 4
let g = 10;
g--;  // g = 9
let h = 100;
h--;  // h = 99
let i = 50;
i--;  // i = 49
let j = -5;
j--;  // j = -6

//   Examples of Postfixis
let x = 5;
let result1 = x++;  // result1 = 5, x = 6
let y = 10;
let result2 = y++;  // result2 = 10, y = 11
let z = 20;
let result3 = z++;  // result3 = 20, z = 21
let w = 30;
let result4 = w++;  // result4 = 30, w = 31
let v = 40;
let result5 = v++;  // result5 = 40, v = 41

// Examples of Prefixis
let m = 5;
let result6 = ++m;  // result6 = 6, m = 6
let n = 10;
let result7 = ++n;  // result7 = 11, n = 11
let o = 20;
let result8 = ++o;  // result8 = 21, o = 21
let p = 30;
let result9 = ++p;  // result9 = 31, p = 31
let q = 40;
let result10 = ++q;  // result10 = 41, q  = 41
